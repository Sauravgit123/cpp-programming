#include <iostream>
using namespace std;

int main()
{
    int i = 1;
    while (i <= 10)
    {
        cout << i << " ";
        i++;
    }

    cout << endl;
    // WHILE LOOP IS SIMILAR TO FOR LOOP ONLY
    int q = 21;
    for (; q <= 10;)
    {
        cout << q << " ";
        q++;
    }

    return 0;
}