#include <iostream>
#include <cmath> // Header file included to use its functions
using namespace std;

int main()
{
    cout << sqrt(4.212) << endl;
    // THESE ARE LIB FUNC, TO USE THEM WE NEED TO INCLUDE HEADER FILES
    cout << cbrt(8) << endl;
    cout << min(8, 5) << endl; // min and max func can be run without cmath lib also
    cout << max(8, 5) << endl;
    cout << pow(3, 3);
    return 0;
}